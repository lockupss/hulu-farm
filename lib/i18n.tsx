const I18nContext = createContext<{ lang: Lang; setLang: (l: Lang) => void; t: (k: string) => string }>({ lang: 'en', setLang: () => {}, t: (k) => k })

export const I18nProvider = ({ children }: { children: React.ReactNode }) => {
  const [lang, setLangState] = useState<Lang>('en')
  useEffect(() => {
    ;(async () => {
      const l = (await loadItem('language')) as Lang
      if (l) setLangState(l)
    })()
  }, [])
  const setLang = (l: Lang) => {
    setLangState(l)
    saveItem('language', l).catch(() => {})
  }
  const t = (k: string) => (translations[lang] && translations[lang][k]) ? translations[lang][k] : translations['en'][k] || k
  return <I18nContext.Provider value={{ lang, setLang, t }}>{children}</I18nContext.Provider>
}

export const useTranslation = () => useContext(I18nContext)

export default useTranslation
